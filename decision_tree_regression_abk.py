import numpy as np
import matplotlib.pyplot as plt
import pandas as pd

"""## Importing the dataset"""

dataset = pd.read_csv('Position_Salaries.csv')
X = dataset.iloc[:, 1:-1].values
y = dataset.iloc[:, -1].values

"""## Training the Decision Tree Regression model on the whole dataset"""

from sklearn.tree import DecisionTreeRegressor
regressor=DecisionTreeRegressor(random_state=0)#ensures that the randomness in the decision tree's training process is reproducible.-about random state=0
regressor.fit(X,y)

"""## Predicting a new result"""

regressor.predict([[6.5]])

"""## Visualising the Decision Tree Regression results (higher resolution)"""

X_grid = np.arange(min(X), max(X), 0.01)#it will create a dense layer or line or series of points ranging from minimum of x to maximum of x withdifference of points being 0.01
X_grid = X_grid.reshape((len(X_grid), 1))#conversion into a single column
plt.scatter(X, y, color = 'red')
plt.plot(X_grid, regressor.predict(X_grid), color = 'blue')#predicting the value for our x grid values
plt.title('Truth or Bluff (Decision Tree Regression)')
plt.xlabel('Position level')
plt.ylabel('Salary')
plt.show()

